# Программа "Щедрый посетитель"

# Ввод суммы счета от пользователя
try:
    сумма_счета = float(input("Введите сумму счета за обед: "))
    
    # Проверяем, что сумма счета больше нуля
    if сумма_счета <= 0:
        print("Сумма счета должна быть положительным числом.")
    else:
        # Рассчитываем чаевые на основе 15% и 20%
        чаевые_15 = сумма_счета * 0.15
        чаевые_20 = сумма_счета * 0.20

        # Выводим результаты
        print(f"Чаевые (15%): {чаевые_15:.2f} руб.")
        print(f"Чаевые (20%): {чаевые_20:.2f} руб.")
        
except ValueError:
    print("Пожалуйста, введите корректное число.")
