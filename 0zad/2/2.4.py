# Программа "Автодилер"

# Ввод стоимости автомобиля без наценок от пользователя
try:
    стоимость_автомобиля = float(input("Введите стоимость автомобиля без наценок: "))
    
    # Проверяем, что стоимость автомобиля больше нуля
    if стоимость_автомобиля <= 0:
        print("Стоимость автомобиля должна быть положительным числом.")
    else:
        # Определяем доли и фиксированные суммы наценок
        налоговая_ставка = 0.1  # 10% налог
        регистрационный_сбор_ставка = 0.05  # 5% регистрационный сбор
        агентский_сбор = 5000  # фиксированный агентский сбор в рублях
        стоимость_доставки = 10000  # фиксированная стоимость доставки в рублях

        # Рассчитываем дополнительные суммы
        налог = стоимость_автомобиля * налоговая_ставка
        регистрационный_сбор = стоимость_автомобиля * регистрационный_сбор_ставка

        # Рассчитываем окончательную стоимость автомобиля
        окончательная_стоимость = стоимость_автомобиля + налог + регистрационный_сбор + агентский_сбор + стоимость_доставки

        # Выводим результаты
        print(f"Начальная стоимость автомобиля: {стоимость_автомобиля:.2f} руб.")
        print(f"Налог (10%): {налог:.2f} руб.")
        print(f"Регистрационный сбор (5%): {регистрационный_сбор:.2f} руб.")
        print(f"Агентский сбор: {агентский_сбор:.2f} руб.")
        print(f"Стоимость доставки: {стоимость_доставки:.2f} руб.")
        print(f"Окончательная стоимость автомобиля: {окончательная_стоимость:.2f} руб.")
        
except ValueError:
    print("Пожалуйста, введите корректное число.")
