import random

def игра_угадай_слово():
    # Список слов для игры
    слова = ["яблоко", "банан", "апельсин", "виноград", "персик"]
    
    # Компьютер выбирает случайное слово
    выбранное_слово = random.choice(слова)
    
    # Сообщаем игроку количество букв в слове
    длина_слова = len(выбранное_слово)
    print(f"Слово состоит из {длина_слова} букв.")
    
    # Игрок получает пять попыток узнать, есть ли буква в слове
    попытки = 5
    использованные_буквы = []
    
    while попытки > 0:
        буква = input("Введите букву: ").strip().lower()
        
        # Проверяем, была ли уже использована эта буква
        if буква in использованные_буквы:
            print("Эта буква уже была использована. Попробуйте другую.")
            continue
        
        использованные_буквы.append(буква)
        
        if буква in выбранное_слово:
            print("Да")
        else:
            print("Нет")
        
        попытки -= 1
        print(f"Осталось попыток: {попытки}")
    
    # Игрок пытается угадать слово
    слово_попытка = input("Попробуйте угадать слово: ").strip().lower()
    
    if слово_попытка == выбранное_слово:
        print("Поздравляем! Вы угадали слово.")
    else:
        print(f"К сожалению, вы не угадали. Загаданное слово было: {выбранное_слово}")

# Запуск игры
игра_угадай_слово()
