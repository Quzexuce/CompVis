def игра_отгадывания():
    print("Загадайте число от 1 до 100. Компьютер будет пытаться его угадать.")
    
    min = 1
    max = 100
    попытка_номер = 0
    
    while min <= max:
        попытка_номер += 1
        guess = (min + max) // 2
        print(f"Попытка {попытка_номер}: Ваше число {guess}?")
        
        # Чтение ответа пользователя и обработка ввода
        ответ = input("Введите 'больше', 'меньше' или 'угадал': ").strip().lower()
        
        if ответ == "угадал":
            print(f"Компьютер угадал число за {попытка_номер} попыток!")
            break
        elif ответ == "больше":
            min = guess + 1
        elif ответ == "меньше":
            max = guess - 1
        else:
            print("Некорректный ввод. Пожалуйста, введите 'больше', 'меньше' или 'угадал'.")

# Запуск игры
игра_отгадывания()
